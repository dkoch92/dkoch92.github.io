function ParseData_M( CallBack, file, Week_Mat){
	Papa.parse(file, {
		download: true,
		header: false,
		dynamicTyping: true,
		complete: function(results) {
			CallBack(results.data,Week_Mat)
		}
	})
}

function CollectData_M( data, mat ) {
	for (var i = 0; i < 12; i++) {
		ii = i+12
		for (var j = 0; j < 3; j++) {
			mat[i][j]   = data[i][j]
			mat[i][j+3] = data[ii][j]
		}
	}
}

