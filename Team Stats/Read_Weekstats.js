function ParseData( CallBack, file, Week_Mat){
	Papa.parse(file, {
		download: true,
		header: false,
		dynamicTyping: true,
		complete: function(results) {
			CallBack(results.data,Week_Mat)
		}
	})
}



function CollectData( data, mat ) {
	for (var i = 0; i < 12; i++) {
		for (var j = 0; j < 12; j++) {
			mat[i][j] =  data[i][j]
		}
	}
}


